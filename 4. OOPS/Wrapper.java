public class Wrapper {

    public static void main(String[] args) {

        // Wrapper class in java

        // Declaring a normal integer variable
        int a = 10;

        // Declaring an integer object using wrapper class
        Integer num = 10;

        // Priting out both values
        System.out.println(a + " " + num);
        // As 'num' here is an object we would get many built in methods with it.

    }
}
